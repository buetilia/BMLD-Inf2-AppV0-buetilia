"Diese App ist ein digitaler Helfer für das Chemielabor. Sie hilft dabei, den **Proteingehalt** in Lebensmitteln (wie Fleisch, Milch oder Getreide) schnell und ohne Taschenrechner auszurechnen."

"* **Rechnen:** Du gibst deine Messwerte aus dem Labor ein, und die App berechnet sofort den Stickstoff- und Proteingehalt."
"* **Mitdenken:** Die App kennt die richtigen Faktoren für verschiedene Lebensmittel (z. B. 6.25 für Fleisch oder 6.38 für Milch)."
"* **Erklären:** In einem eigenen Bereich wird Schritt für Schritt erklärt, wie die Kjeldahl-Methode im Labor funktioniert."

"Im Labor muss man oft komplizierte Formeln benutzen. Dabei passieren leicht Tippfehler. Diese App übernimmt das Rechnen, verhindert Fehler und zeigt die Ergebnisse übersichtlich an."

"1. **Einstellungen:** Wähle links in der Seitenleiste aus, was du untersucht hast (z. B. Milch)."
"2. **Eingabe:** Gib im Rechner deine Zahlen ein (Einwaage und Verbrauch der Säure)."
"3. **Ergebnis:** Klicke auf *Berechnen* und du siehst sofort das Resultat."

"*Entwickelt für das Modul Informatik 2 an der ZHAW.*"
